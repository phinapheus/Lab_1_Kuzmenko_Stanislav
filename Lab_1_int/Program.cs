int a = 8;
int b = 2;
Console.WriteLine("Unary operations with int");
Console.WriteLine(++a);
Console.WriteLine(--b);
Console.WriteLine("Binary operations with int");
Console.WriteLine(a + b);
Console.WriteLine(a - b);
Console.WriteLine(a * b);
Console.WriteLine(a / b);
Console.WriteLine(a % b);
Console.WriteLine("Ternary operation with int");
Console.WriteLine(a > b ? a + b : a - b);
        